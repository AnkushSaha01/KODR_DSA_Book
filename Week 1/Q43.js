const prompt = require("prompt-sync")();

//Question 22 using Switch (Check Consonant or Vowel using Switch)

const char = prompt("Enter a character: ");
switch (char) {
    case "a":
        console.log("Vowel");
        break;
    case "e":
        console.log("Vowel");
        break;
    case "i":
        console.log("Vowel");
        break;
    case "o":
        console.log("Vowel");
        break;
    case "u":
        console.log("Vowel");
        break;
    default:
        console.log("Consonant");
}





